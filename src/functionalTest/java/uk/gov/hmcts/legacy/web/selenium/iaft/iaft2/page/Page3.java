	public static final String POINTS_BASED_YES_XPATH = "//input[@name='s2a' and @value='Yes']";

	public static final String POINTS_BASED_NO_XPATH = "//input[@name='s2a' and @value='No']";

	public static final String SERVICE_DECSION_METHOD_POST_XPATH = "//input[@name='s2d' and @value='Post']";

	public static final String SERVICE_DECSION_METHOD_FAX_XPATH = "//input[@name='s2d' and @value='Fax']";

	public static final String SERVICE_DECSION_METHOD_COURIER_XPATH = "//input[@name='s2d' and @value='Courier']";

	public static final String SERVICE_DECSION_METHOD_OTHER_XPATH = "//input[@name='s2d' and @value='Other']";

	public static final String POST_REFERENCE_NUMBER_NAME ="s2b";

	public static final String POST_MAKING_DECISON_NAME = "s2c";

	public static final String SPECIFY_OTHER_NAME = "s2d_other";

	public static final String REFUSAL_DAY_NAME = "s2e_day";

	public static final String REFUSAL_MONTH_NAME = "s2e_month";

	public static final String REFUSAL_YEAR_NAME = "s2e_year";

	public static final String NOTICE_DAY_NAME = "s2i_day";

	public static final String NOTICE_MONTH_NAME = "s2i_month";

	public static final String NOTICE_YEAR_NAME = "s2i_year";

	@Override
	public void execute() {
		logger.info("Executing IAFT2 Step 3");
		int fieldLength = fields.length;
		if (fieldLength == 12) {

			String val = fields[0];
			if (null != val && val.equals(POINTS_BASED_YES_XPATH) || val.equals(POINTS_BASED_NO_XPATH)) {
				findByXPathAndClick(val);
			} else {
				logger.info("Val:" + val + " not understood expected one of:" + POINTS_BASED_NO_XPATH + " "
						+ POINTS_BASED_YES_XPATH);
			}

			val = fields[1];
			if (null != val) {
				findByNameAndEnterText(POST_REFERENCE_NUMBER_NAME, val);
			}

			val = fields[2];
			if (val != null) {
				if(!val.equals("NA"))
				findSelectByNameAndSelectByValue(POST_MAKING_DECISON_NAME, val);
			} else {
				for (String post : POSTS) {
					findSelectByNameAndSelectByValue(POST_MAKING_DECISON_NAME, post);
				}
			}

			val = fields[3];
			if (null != val && val.equals(SERVICE_DECSION_METHOD_POST_XPATH)) {
				findByXPathAndClick(SERVICE_DECSION_METHOD_POST_XPATH);
			} else if (null != val && val.equals(SERVICE_DECSION_METHOD_FAX_XPATH)) {
				findByXPathAndClick(SERVICE_DECSION_METHOD_FAX_XPATH);
			} else if (null != val && val.equals(SERVICE_DECSION_METHOD_COURIER_XPATH)) {
				findByXPathAndClick(SERVICE_DECSION_METHOD_COURIER_XPATH);
			} else if (null != val && val.equals(SERVICE_DECSION_METHOD_OTHER_XPATH)) {
				findByXPathAndClick(SERVICE_DECSION_METHOD_OTHER_XPATH);
			}

			val = fields[4];
			if (null != val) {
				findByNameAndEnterText(SPECIFY_OTHER_NAME, val);
			}

			val = fields[5];
			if (val != null) {
				if(!val.equals("NA"))
				findSelectByNameAndSelectByValue(REFUSAL_DAY_NAME, val);
			} else {
				for (String day : DAYS) {
					findSelectByNameAndSelectByValue(REFUSAL_DAY_NAME, day);
				}
			}

			val = fields[6];
			if (val != null) {
				if(!val.equals("NA"))
				findSelectByNameAndSelectByValue(REFUSAL_MONTH_NAME, val);
			} else {
				for (String month : MONTHS) {
					findSelectByNameAndSelectByValue(REFUSAL_MONTH_NAME, month);
				}
			}

			val = fields[7];
			if (val != null) {
				if(!val.equals("NA"))
				findSelectByNameAndSelectByValue(REFUSAL_YEAR_NAME, val);
			} else {
				for (String year : TWOMINUS_YEARS) {
					findSelectByNameAndSelectByValue(REFUSAL_YEAR_NAME, year);
				}
			}

			val = fields[8];
			if (val != null) {
				if(!val.equals("NA"))
				findSelectByNameAndSelectByValue(NOTICE_DAY_NAME, val);
			} else {
				for (String day : DAYS) {
					findSelectByNameAndSelectByValue(NOTICE_DAY_NAME, day);
				}
			}

			val = fields[9];
			if (val != null) {
				if(!val.equals("NA"))
				findSelectByNameAndSelectByValue(NOTICE_MONTH_NAME, val);
			} else {
				for (String month : MONTHS) {
					findSelectByNameAndSelectByValue(NOTICE_MONTH_NAME, month);
				}
			}

			val = fields[10];
			if (val != null) {
				if(!val.equals("NA"))
				findSelectByNameAndSelectByValue(NOTICE_YEAR_NAME, val);
			} else {
				for (String year : TWOMINUS_YEARS) {
					findSelectByNameAndSelectByValue(NOTICE_YEAR_NAME, year);
				}
			}

			val = fields[11];
			if (null != val && val.equals(NavigateEnum.NEXT.getValue())) {
				clickNextButton();
			} else if (null != val && val.equals(NavigateEnum.PREVIOUS.getValue())) {
				clickPreviousButton();
			} else {
				logger.info("Val:" + val + " not understood expected one of:" + NavigateEnum.NEXT.getValue()
						+ " " + NavigateEnum.PREVIOUS.getValue());
			}

		} else {
			logger.info("ConfirmRepayment not initialised with correct number of fields. Expected 12 was "
					+ fieldLength);
		}

	}

